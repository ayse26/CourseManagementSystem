public class Register {
    private int id;
    private String name;
    private String lastname;

    private Lesson lesson;
    private int age;

    public Register(){
    }

    public Register(String name, String lastname, Lesson lesson, int age) {
        this.name = name;
        this.lastname = lastname;
        this.lesson = lesson;
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Lesson getLesson() {
        return lesson;
    }

    public void setLesson(Lesson lesson) {
        this.lesson = lesson;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
